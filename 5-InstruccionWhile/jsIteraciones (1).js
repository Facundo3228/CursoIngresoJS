/**
 * Enunciado:
 * al presionar el botón mostrar 10 repeticiones con números
 * ASCENDENTE, desde el 1 al 10.
 */

function mostrar()
{
	//variables
	var contador;

	//inicio de variable
	contador = 0;

	//ciclo while
	while (contador < 10) {
		contador++;
		alert("Contador en: " + contador);
	}
}//FIN DE LA FUNCIÓN