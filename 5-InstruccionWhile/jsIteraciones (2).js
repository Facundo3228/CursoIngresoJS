/**
 * Enunciado:
 * al presionar el botón mostrar 10 repeticiones con números
 * DESCENDENTES, desde el 10 al 1.
 */

function mostrar()
{
	//variables
	var contador;

	//inicio de variable
	contador = 10;

	//inicio del while
	while (contador > 0) {
		alert("Contador en: " + contador);
		contador--;		
	}
}//FIN DE LA FUNCIÓN